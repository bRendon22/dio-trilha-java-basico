public class ExemploArray {
    public static void main(String[] args) {
        
            // Array e seu indice de elementos começa em 0 
            String alunos [] = {"FELIPE", "JONAS", "BRENDON", "MONISE"};

            //Enquanto x que inicia em 0 for menor que o comprimento da matriz o array ele vai repetir seguindo seu incremento de +1 
            for (int x=0; x< alunos.length; x++ ) {     
                System.out.println("O aluno no indice x = " + x + " é " + alunos[x]);

            }



    }
}
